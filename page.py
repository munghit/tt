import streamlit as st

st.set_page_config(
    page_title="평균 계산 과정",
    page_icon="📊",
    layout="wide"
)

st.title("📊 평균 계산 과정")

st.markdown("---")

st.subheader("1️⃣ 장소별 · 시간대별 소음 데이터 분류")

st.info("""
측정된 소음 데이터를

- 장소별(교실, 다목적실, 탑티어실)
- 시간대별(쉬는 시간, 점심시간, 야자시간)

로 분류합니다.
""")

st.subheader("2️⃣ 평균 계산")

st.latex(r"""
\text{평균(dB)}
=
\frac{\sum \text{소음값}}{\text{데이터 개수}}
""")

st.code("""
예시)

교실(점심시간)

51.2
50.8
52.0
51.1

↓

(51.2 + 50.8 + 52.0 + 51.1)

÷

4

=

51.27 dB
""")

st.subheader("3️⃣ 계산 결과 정리")

st.table({
    "장소":["교실","다목적실","탑티어실"],
    "쉬는 시간 평균":["47.34dB","48.75dB","29.79dB"],
    "점심시간 평균":["51.27dB","45.68dB","32.52dB"],
    "야자 시간 평균":["자료 없음","34.39dB","40.66dB"]
})

st.subheader("4️⃣ 결과 분석")

st.success("""
• 점심시간에는 탑티어실이 가장 조용합니다.

• 교실은 점심시간 평균이 가장 높아 가장 시끄럽습니다.

• 다목적실은 시간이 지날수록 소음이 감소하는 경향을 보입니다.

• 탑티어실은 쉬는 시간과 점심시간에는 조용하지만 야자시간에는 소음이 증가합니다.
""")
